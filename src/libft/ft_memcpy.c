#include <libft.h>

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && (dest != NULL || src != NULL))
	{
		((char *) dest)[i] = ((const char *) src)[i];
		i++;
	}
	return (dest);
}
