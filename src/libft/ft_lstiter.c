#include <libft.h>

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*lst_tmp;

	lst_tmp = lst;
	if (!f || !lst)
		return ;
	while (lst)
	{
		(f)(lst_tmp->content);
		lst_tmp = (lst->next);
		lst = lst_tmp;
	}
}
