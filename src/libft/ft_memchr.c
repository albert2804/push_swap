#include <libft.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	int				i;
	unsigned char	*str;
	unsigned char	chr;

	str = (unsigned char *)s;
	chr = (unsigned char)c;
	i = 0;
	while (n > 0)
	{
		if (str[i] == chr)
			return (str + i);
		i++;
		n--;
	}
	return (0);
}
