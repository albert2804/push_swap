#include <libft.h>

size_t	ft_strlcpy(char *dest, const char *src, size_t dst_size)
{
	size_t	i;
	int		counter;

	counter = 0;
	i = 0;
	if (dst_size > 0)
	{
		while (i < (dst_size - 1) && src[i] != '\0')
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	while (src[counter] != '\0')
		counter++;
	return (counter);
}
