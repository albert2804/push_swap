#include <libft.h>

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	len;

	i = 0;
	len = ft_strlen(s);
	while (s[i] != (const char)c)
	{
		if (i == len)
			return (0);
		i++;
	}
	return ((char *)s + i);
}
